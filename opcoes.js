let camiseta=document.querySelector("#camiseta")
let stamp=document.querySelector("#estampa")


function mudaLaranja () {
    camiseta.style.fill="orange"
    
    stamp.style.animation="aparece 2s"
}

function mudaPreto () {
    camiseta.style.fill="#ccc"
   
    stamp.style.animation="aparece 2s"
}

function mudaVermelho () {
    camiseta.style.fill= "#881607"
 
    stamp.style.animation="aparece 2s"
}